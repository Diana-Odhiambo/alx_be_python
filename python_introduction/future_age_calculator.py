# This script calculates the future age of a person in the year 2050

# Ask the user for their current age
current_age = int(input("How old are you? "))

# Calculate the age in 2050 (2023 + 27 years)
future_age = current_age + 27

# Print the result
print("In 2050, you will be", future_age, "years old.")
